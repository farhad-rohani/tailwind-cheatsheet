function htmlFile($folder, mixed $value, $j): void
{
    $address = $folder . '/' . $j . '_' . $value . '.html';
    chmod($address, 0777);
    $myfile = fopen($address, "w") or die("Unable to open file!");
    $txt = getStr($value);
    fwrite($myfile, $txt);
    fclose($myfile);
}

