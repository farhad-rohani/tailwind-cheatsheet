/**
 * @param array $list
 * @return void
 */
function makeFile(array $list): void
{
    $i = 1;
    foreach ($list as $key => $item) {
        echo $key . '</br>';
        $folder = $i . '_' . $key;
        mkdir($folder, 0777, true);
        $j = 1;
        foreach ($item as $index => $value) {
            if ($index =='Meta') {
                continue;
            }
            htmlFile($folder, $index, $j);
            mkvFile($folder, $index, $j);
            psdFile($folder, $index, $j);
            jpgFile($folder, $index, $j);
            $j++;
        }
        $i++;
    }
}

