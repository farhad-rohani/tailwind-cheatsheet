/**
 * @param int|string $key
 * @param mixed $value
 * @return void
 */
function mkvFile($folder, mixed $value, $j): void
{
    $address = $folder . '/' . $j . '_' . $value . '.mkv';
    chmod($address, 0777);
    $myfile = fopen($address, "w") or die("Unable to open file!");
    fclose($myfile);
}

function psdFile($folder, mixed $value, $j): void
